import json
import time

import requests


def main():
    """
    Скрипт вне контейнера.
    Отправляет 10 запросов на FastAPI-обертку MCP-сервиса и сохраняет отчет инференса.
    """
    print("Запуск прогона 10 запросов...")

    queries = [
        # --- SHORT: Слишком короткие (менее 4 слов) ---
        "Слепой рыжий кот",
        "Агрессивный питбуль",
        "Щенок",
        # --- AVERAGE: Средние описания (от 4 до 9 слов) ---
        "Маленький рыжий котенок, найден на улице",
        "Черный щенок дворняги, пугливый, но добрый",
        "Собака породы мопс, есть проблемы с дыханием",
        "Взрослая хаски, очень активная и громкая",
        # --- GOOD: Отличные подробные описания (10 и более слов) ---
        "Ласковая кошка Маруся, 2 года, стерилизована, приучена к лотку, обожает сидеть на ручках и громко мурчать.",
        "Пожилой лабрадор Барон. Очень спокойный, дружелюбен к детям и другим собакам, нуждается в поддерживающей терапии для суставов.",
        "Белоснежный кролик с красными глазами, возраст около года, питается исключительно свежей зеленью и специальным кормом, требует просторной клетки.",
    ]

    report = []

    for i, q in enumerate(queries, 1):
        print(f"[{i}/10] Запрос: {q}")
        try:
            # Обращаемся к контейнеру MCP-сервиса
            resp = requests.post("http://localhost:8001/process", json={"prompt": q})
            data = resp.json()

            report.append(
                {
                    "Запрос к LLM": q,
                    "Вывод LLM": data.get("llm_verdict"),
                }
            )
        except Exception as e:
            print(f"Ошибка при запросе {q}: {e}")

        time.sleep(1)

    # Формирование отчета инференса
    with open("inference_report.json", "w", encoding="utf-8") as f:
        json.dump(report, f, ensure_ascii=False, indent=4)

    print("\nОтчет инференса успешно сохранен в 'inference_report.json'.")


if __name__ == "__main__":
    main()
